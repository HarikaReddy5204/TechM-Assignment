package collections;
import java.util.ArrayList;
public class arraylist {
	 public static void main(String[] args) {
		 ArrayList<String> colors = new ArrayList<>();
		 colors.add("Red");
	        colors.add("Blue");
	        colors.add("Green");
	        colors.add("Yellow");
	        colors.add("Purple");
	        for(String color : colors) {
	            System.out.println(color);
		 
	 }
	
	 }
}
